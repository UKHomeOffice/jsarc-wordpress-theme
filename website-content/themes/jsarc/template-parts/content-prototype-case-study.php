<section class="section section-case-study-content">
	<div class="section-content-wrapper">
		<div class="section-content">
      <h2 class="heading">The Challenge</h2>
      
      <p class="body-text">Due to the developing global terrorist threat, as demonstrated in Manchester and London, preventing mass casualty attacks is a priority. Although the UK is considered a leader in the security field, there is an appetite in the Home Office and other government departments to work more closely with industry to improve the detection capabilities and security of crowded places in the UK. 

      <p class="body-text">The aim is to allow companies to promote an understanding and awareness of how innovative technologies and operational concepts can be combined to reduce the risk of attacks. This can be achieved without inconveniencing the public or impeding business. 

      <p class="body-text">JSaRC wants venues to provide an enhanced visitor experience through easy access combined with assuring visitors about their own security. This involves screening large numbers of people quickly, cost-effectively and proportionately whilst retaining the confidence that no-one entering a crowded space represents a security threat.  

      <p class="body-text">JSaRC is looking to show an alternative to airport-style security. This will provide an insight into the developments in security technology to a previously unused audience.
      </div>
	</div>

</section>



<section class="section bg-black full-height section-case-study-content">
	<div class="sticky-container">
