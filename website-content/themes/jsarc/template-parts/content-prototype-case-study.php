  <div class="section-content-wrapper full-height">
		<div class="section-content on-dark-bg">
    
      
      <h2 class="heading">Outcomes</h2>

      <p class="body-text">The trials proved to be a success and proved that a small lightweight programme can deliver useful and worthwhile trials. They have given manufacturers valuable exposure and have provided site operators with useful insight. Additionally, CPNI is using the learning from the trials to inform guidance development.

      <p class="body-text">Exposure was of great importance to the manufacturers. The trials provided a great opportunity to raise their profile and build relationships with potential customers. The trials enabled the identification of new areas where existing equipment can be adapted. There is now a greater understanding of end user requirements through realistic operational data, which feeds into product development.  

      <p class="body-text">For venues, the trials provided valuable insight into a technology’s capabilities and its limitations. This lead to improved confidence in the robustness of the technology and its fitness for service. The trials showed that it is possible for venues to improve customer safety seamlessly, unobtrusively and affordably.  This in turn provided reassurance that high footfall screening is feasible. The opportunity to be able to communicate requirements to developers was also critical.

      <p class="body-text">The interest being shown by major attractions, shopping centres and arenas is now far greater, which will no doubt support the growth of the security sector.

      <p class="body-text">Stakeholders are starting to buy-in to the ‘screen everyone for something’ approach. If this is coupled with the priceless feedback given to manufacturers who are addressing the need for high footfall, low interference and cost-effective technologies, the trials can be said to have successfully driven the delivery of the right solution as well as supporting the growth of the security sector.



		</div>
	</div>

