	<div class="section-content-wrapper full-height">
    <div class="section-content on-dark-bg">
      <h2 class="heading">Solutions implemented</h2>

      <p class="body-text">JSaRC, partnered with CPNI, Iconal and Fenley Martel, held a series of trials showcasing high footfall screening technologies at various crowded places across the UK. This event took place following a successful initial Innovation Call. 

      <p class="body-text">The focus of the trials was to provide an insight into how imaginative and proportional approaches can manage risks related to detecting threat items in crowds. The trials explored how this could be achieved without the usual delays caused by intrusive security processes, such as divesting clothing, which inconvenience visitors and businesses.    

      <p class="body-text">While innovative technologies are becoming more available, their adoption has been limited. This is partly due to a lack of understanding of what benefits these technologies offer and how they can be used in real world applications. 

      <p class="body-text">Consequently, the limited take-up of new approaches and technologies is often inhibiting, delaying or completely preventing the exploitation of new capabilities that may succeed in mitigating security threats. 

    </div>
	</div>

</section>


<section class="section section-case-study-content">
  <figure class="section-image image1"></figure>
  <div class="section-content-wrapper">
    <div class="section-content ">
        <figure class="in-page-image">
          <img src="../wp-content/themes/jsarc/img/v/case-study/a/case-study-results-image.png" alt="[image alt text]">
        </figure>
        <h2 class="heading">Results</h2>
        <p class="body-text">In total, 5 sites and 6 technology providers volunteered to participate in trials.  

        <p class="body-text">The trials involved real users, which demonstrated a real world impact. This helped to engage all stakeholder groups.

        <p class="body-text">The chosen sites represented typical crowded places. The sites all sought to combine a high level of security assurance with minimum interference for their visitors and existing security procedures. Each site was individually informed as to how to develop the optimal concepts of their operation; this was a process that combined people, processes and technology.  

        <p class="body-text">The technology providers sought to understand how their equipment worked best in the field. These demonstrations provided business stakeholders with an important opportunity as ‘the point of the trial is not just the kit, but everything around the kit.’  

        <p class="body-text">The trials directed product design and development in a totally unique way, leading one technology provider to comment that the product development lessons learned in a 6 week trial is equivalent to a year of market research.  

        <p class="body-text">Another stakeholder commented that the trials give them ‘knowledge of real life applications in front of customers’ and were the most productive use of government support they had had over the 12 year period their product has been in development.  

        <p class="body-text">It was the first time that they had seen their system used in test for true high footfall screening, despite having many tens of systems in the field. They estimated that the knowledge gained had accelerated the time to market of their latest version by 18 months (with a £1-2M potential payback). 
      </div>
  </div>

</section>







<section class="section bg-black full-height section-case-study-content">
  <div class="sticky-container">
