	var stickyContainer = document.querySelector('.sticky-container'),
		stickyItem = stickyContainer.querySelector('.sticky-item');
	
	var r = function() {
		var rect = stickyContainer.getBoundingClientRect();
		elX = rect.left;
		elY = rect.top;
		elW = rect.width;
		elH = rect.height;
		elB = elY + elH;
		var h = window.innerHeight || document.documentElement.clientHeight || document.body.clientHeight;
	}
	r();

	window.addEventListener('resize', function() {
		r();
	});

	window.addEventListener('scroll', function() {
		var y = window.scrollY || window.pageYOffset;
		var calc = (elH - y + elY) / elH;
		if(y > elY && y < elB) {
			stickyItem.style.opacity = calc;
			stickyItem.style.position = 'fixed';
			if (calc > 1) {
				stickyItem.style.opacity = 1;
			}
			else if (calc < 0) {
				stickyItem.style.opacity = 0;
			}
		}
		else {
			stickyItem.style.opacity = 1;
			stickyItem.style.position = 'relative';
		}
	});
	
	
	
	
	
	
	
	
